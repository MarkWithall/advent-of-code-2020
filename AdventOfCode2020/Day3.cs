        private static int CountTrees(string[] map, int right, int down)
        {
            var treeCount = 0;
            var currentX = 0;
            var currentY = 0;
            var mapWidth = map[0].Length;
            var mapHeight = map.Length;
            while (currentY < mapHeight)
            {
                var currentCell = map[currentY][currentX];
                if (currentCell == '#')
                {
                    treeCount++;
                }

                currentX = (currentX + right) % mapWidth;
                currentY += down;
            }

            return treeCount;
        }

